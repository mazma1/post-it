import React from 'react';
import { connect } from 'react-redux';
import isEmpty from 'lodash/isEmpty';
import toastr from 'toastr';
import { bindActionCreators } from 'redux';
import PropTypes from 'prop-types';
import { postNewMessage } from '../../actions/groupMessages';

/**
 * Message Input Form component for message board
 */
class MsgForm extends React.Component {
  /**
   * Constructor
   * @param {object} props
   */
  constructor(props) {
    super(props);

    this.state = {
      messageInput: '',
      priority: 'normal',
      errors: {}
    };

    this.onChange = this.onChange.bind(this);
    this.onSelect = this.onSelect.bind(this);
    this.onMessageSend = this.onMessageSend.bind(this);
  }

  /**
   * Handles change event of New Group input form
   * Updates messageInput state
   * @param {SyntheticEvent} e
   * @returns {void}
   */
  onChange(e) {
    this.setState({ [e.target.name]: e.target.value });
  }

   /**
   * Handles change event of Priority Select input
   * Updates messageInput state
   * @param {SyntheticEvent} e
   * @returns {void}
   */
  onSelect(e) {
    this.setState({ [e.target.name]: e.target.value });
  }

  /**
   * Handles Message Send event
   * Dispatches PostNewMessage action if a group exists and message is not empty
   * @param {SyntheticEvent} e
   * @returns {void}
   */
  onMessageSend(e) {
    e.preventDefault();
    const { groupId, username } = this.props;
    if (groupId && this.state.messageInput !== '') {
      const userMessage = this.state.messageInput;
      const validFlag = ['urgent', 'normal', 'critical'];
      const priority = userMessage.split(' ').shift().slice(1);

      if (validFlag.includes(priority)) {
        const messageBody = userMessage.slice(priority.length + 1);
        this.props.postNewMessage({
          message: messageBody,
          priority,
          group_id: groupId,
          read_by: username
        }).then(() => {
          this.setState({ messageInput: '' });
        }).catch(() => {
          toastr.error('Unable to send message, please try again');
        });
      } else {
        this.props.postNewMessage({
          message: this.state.messageInput,
          priority: this.state.priority,
          group_id: groupId,
          read_by: username
        }).then(() => {
          this.setState({ messageInput: '' });
        }).catch(() => {
          toastr.error('Unable to send message, please try again');
        });
      }
    }
  }

  /**
   * Render
   * @returns {ReactElement} Message Input Form markup
   */
  render() {
    if (isEmpty(this.props.selectedGroup)) {
      return null;
    }
    return (
      <footer className="footer">
        <div className="footer-container">
          <form className="form-horizontal" id="chatform" onSubmit={this.onMessageSend}>
            <div className="form-group">
              <div className="col-lg-8 col-md-10 col-sm-11 col-xs-11 msg-form-container">
                <input
                  className="form-control"
                  type="text"
                  id="m"
                  name="messageInput"
                  value={this.state.messageInput}
                  onChange={this.onChange}
                  placeholder="Enter your message..."
                  autoComplete="off"
                />
              </div>

              <div className="col-lg-3 col-md-10 col-sm-11 col-xs-11 priority-container">
                <label>Priority</label>
                <select className="browser-default" name="priority" onChange={this.onSelect} value={this.state.priority}>
                  <option value="normal">Normal</option>
                  <option value="urgent">Urgent</option>
                  <option value="critical">Critical</option>
                </select>
              </div>

              <div className="col-lg-1 col-md-2 col-sm-1 col-xs-1 msg-send-btn">
                <a
                  href='#'
                  type="submit"
                  onClick={this.onMessageSend}
                  className="btn waves-effect waves-light blue lighten-1">
                  Send
                </a>
              </div>
            </div>
          </form>
        </div>
      </footer>
    );
  }
}


/**
 * Maps pieces of the redux state to props
 * @param {object} state Redux state
 * @returns {object} User id and selected group id
 */
function mapStateToProps(state) {
  return {
    groupId: state.selectedGroup.id,
    username: state.signedInUser.user.username,
    selectedGroup: state.selectedGroup
  };
}


/**
 * Maps action creators to redux dispatch function
 * Action creators bound will be available as props in Sidebar
 * Actions generated by the action creators flows though all the reducers
 * @param {function} dispatch Redux dispatch function
 * @returns {function} Action cretaors bound to redux dispatch function
 */
function mapDispatchToProps(dispatch) {
  return bindActionCreators({
    postNewMessage
  }, dispatch);
}


MsgForm.propTypes = {
  userId: PropTypes.number,
  groupId: PropTypes.number,
  postNewMessage: PropTypes.func.isRequired
};

export default connect(mapStateToProps, mapDispatchToProps)(MsgForm);
